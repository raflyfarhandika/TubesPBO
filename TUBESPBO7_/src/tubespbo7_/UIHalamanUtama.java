    private void TambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TambahMouseClicked
        // TODO add your handling code here:\
        
        int index = List.getSelectedIndex();
        MHalamanUtama data = LBarang.get(index);
        
        try{
            DB_Connection.Connection();
            String sql = "INSERT INTO pembelian(nama,harga,jumlah,kategori) VALUES(?,?,?,?)";
            PreparedStatement ps = DB_Connection.conn.prepareStatement(sql);
            ps.setString(1, NamaBarang.getText());
            ps.setString(2, Harga.getText());
            ps.setString(3, Jumlah.getText());
            ps.setString(4, data.getKategori());
            ps.execute();
            
            JOptionPane.showMessageDialog(this, "Berhasil Ditambahkan");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_TambahMouseClicked

