    private void FillListBoxPakaian(){
        DB_Connection.Connection();
        
        String sql = "SELECT * FROM barang";
        
        DefaultListModel data = new DefaultListModel();
        
        try {
            ps = DB_Connection.conn.prepareStatement(sql);
            rs = ps.executeQuery();
            
            while(rs.next()){
                
                String _nama_barang = rs.getString("nama_barang");
                String _kategori_barang = rs.getString("kategori_barang");
                int _harga_barang = rs.getInt("harga_barang");
                data.addElement(_nama_barang);
                LBarang.add(new MHalamanUtama(rs.getString("nama_barang"), rs.getInt("harga_barang"), rs.getString("kategori_barang"))); //rs.getString("kategori_barang")));
            }
                List.setModel(data);
            
            //rs.close();
            //DB_Connection.conn.close();
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    };
